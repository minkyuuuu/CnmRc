package com.cnm.cnmrc.parser;

public class SearchProgram {

	private String id = "";
	private String number = "";
	private String name = "";
	private String info = "";
	private String logoImg = "";
	private String programId = "";
	private String programTime = "";
	private String programNextTime = "";
	private String programTitle = "";
	private String programSeq = "";
	private String programGrade = "";
	private String programHD = "";
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public String getLogoImg() {
		return logoImg;
	}
	public void setLogoImg(String logoImg) {
		this.logoImg = logoImg;
	}
	public String getProgramId() {
		return programId;
	}
	public void setProgramId(String programId) {
		this.programId = programId;
	}
	public String getProgramTime() {
		return programTime;
	}
	public void setProgramTime(String programTime) {
		this.programTime = programTime;
	}
	public String getProgramNextTime() {
		return programNextTime;
	}
	public void setProgramNextTime(String programNextTime) {
		this.programNextTime = programNextTime;
	}
	public String getProgramTitle() {
		return programTitle;
	}
	public void setProgramTitle(String programTitle) {
		this.programTitle = programTitle;
	}
	public String getProgramSeq() {
		return programSeq;
	}
	public void setProgramSeq(String programSeq) {
		this.programSeq = programSeq;
	}
	public String getProgramGrade() {
		return programGrade;
	}
	public void setProgramGrade(String programGrade) {
		this.programGrade = programGrade;
	}
	public String getProgramHD() {
		return programHD;
	}
	public void setProgramHD(String programHD) {
		this.programHD = programHD;
	}

}
