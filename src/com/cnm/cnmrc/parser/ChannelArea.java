package com.cnm.cnmrc.parser;

public class ChannelArea {
	
	private String areaCode;
	private String areaName;
	private String areaNameDetail;
	
	
	
	public String getAreaCode() {
		return areaCode;
	}

	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}

	public String getAreaName() {
		return areaName;
	}

	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}

	public String getAreaNameDetail() {
		return areaNameDetail;
	}

	public void setAreaNameDetail(String areaNameDetail) {
		this.areaNameDetail = areaNameDetail;
	}
	
}
